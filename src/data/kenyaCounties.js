const sanitizeCountyName = (value) =>
  value
    .toLowerCase()
    .normalize("NFKD")
    .replace(/[\u0300-\u036f]/g, "")
    .replace(/[^a-z]/g, "");

const COUNTY_LOOKUP = RAW_COUNTIES.reduce((acc, name) => {
  acc[sanitizeCountyName(name)] = name;
  return acc;
}, {});

export const normalizeCountyName = (value) => {
  if (typeof value !== "string") return null;
  const cleaned = sanitizeCountyName(value);
  if (!cleaned) return null;
  return COUNTY_LOOKUP[cleaned] || null;
};

export const KENYA_COUNTIES = RAW_COUNTIES;
